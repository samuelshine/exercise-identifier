# ─── LLM descriptor generation ───────────────────────────────────────────────

DESCRIPTOR_PROMPT = """\
You are a fitness content writer for an exercise identification app. \
Users identify exercises by typing what they SAW or what they would say to a friend.

Exercise: {name}
Primary muscles: {muscles}
Equipment used: {equipment}
Movement type: {pattern}

Generate the following content and return as a JSON object (no other text):

"summary": A precise 1-2 sentence technical description a personal trainer would use.

"beginner_descriptions": Array of exactly 4 colloquial descriptions. Write them as a \
gym-goer who has no idea what the exercise is called would describe what they saw or experienced. \
Use vague, layperson language. Examples of good beginner_descriptions:
 - "that thing where you sit and pull the bar down to your chest from above"
 - "lying on a bench and pushing two dumbbells up toward the ceiling"
 - "bending over and pulling a bar up toward your belly button"
 - "the machine where your legs push two footplates away from you"

"cues": Array of exactly 2 brief coaching form cues (10-15 words each, actionable).

"common_mistake": 1-2 sentences describing the most common error beginners make.\
"""


@retry(
    retry=retry_if_exception_type(Exception),
    stop=stop_after_attempt(3),
    wait=wait_exponential(multiplier=1, min=1, max=5),
    reraise=True,
)
async def generate_descriptors(ex: ExDef, ollama_client) -> dict:
    """Call the LLM to generate all descriptor texts for one exercise."""
    settings = get_settings()

    prompt = DESCRIPTOR_PROMPT.format(
        name=ex.name,
        muscles=", ".join(m.value for m in ex.primary),
        equipment=", ".join(e.value for e in ex.equipment),
        pattern=ex.pattern.value,
    )

    response = await asyncio.wait_for(
        ollama_client.generate(
            model=settings.ollama_judge_model,
            prompt=prompt,
            format="json",
            options={"temperature": 0.7},  # higher temp for diverse descriptions
        ),
        timeout=15.0,
    )

    return json.loads(response["response"])


# ─── Database operations ─────────────────────────────────────────────────────

async def insert_exercise(session, ex: ExDef, descriptors: dict) -> Exercise | None:
    """Insert a single exercise with all relations. Returns None if already exists."""
    from sqlalchemy import select
    from sqlalchemy.exc import IntegrityError

    # Check for existing slug — idempotent
    existing = await session.scalar(select(Exercise).where(Exercise.slug == ex.slug))
    if existing:
        return None

    exercise = Exercise(
        primary_name=ex.name,
        slug=ex.slug,
        difficulty=ex.difficulty,
        mechanic=ex.mechanic,
        force_type=ex.force,
        movement_pattern=ex.pattern,
        is_unilateral=ex.unilateral,
        summary=descriptors.get("summary", ""),
    )
    session.add(exercise)

    try:
        await session.flush()
    except IntegrityError:
        await session.rollback()
        return None

    # Muscles
    for mg in ex.primary:
        session.add(ExerciseMuscleGroup(exercise_id=exercise.id, muscle_group=mg, is_primary=True))
    for mg in ex.secondary:
        session.add(ExerciseMuscleGroup(exercise_id=exercise.id, muscle_group=mg, is_primary=False))

    # Equipment
    for eq in ex.equipment:
        session.add(ExerciseEquipment(exercise_id=exercise.id, equipment_type=eq))

    # Aliases
    for alias_text in ex.aliases:
        session.add(ExerciseAlias(exercise_id=exercise.id, alias=alias_text))

    # Descriptors from LLM
    desc_map = [
        (DescriptorCategory.SUMMARY, descriptors.get("summary", "")),
        *[(DescriptorCategory.BEGINNER_DESCRIPTION, d) for d in descriptors.get("beginner_descriptions", [])],
        *[(DescriptorCategory.CUE, c) for c in descriptors.get("cues", [])],
        (DescriptorCategory.COMMON_MISTAKE, descriptors.get("common_mistake", "")),
    ]
    for category, text in desc_map:
        if text and text.strip():
            session.add(MovementDescriptor(
                exercise_id=exercise.id,
                category=category,
                text=text.strip(),
                needs_reindex=True,
            ))

    await session.commit()
    return exercise


# ─── Main pipeline ────────────────────────────────────────────────────────────

async def run(dry_run: bool = False, skip_existing: bool = True) -> None:
    from ollama import AsyncClient
    from app.core.database import async_session_factory

    settings = get_settings()
    client = AsyncClient(host=settings.ollama_host)

    logger.info("Starting dataset generation — %d exercises defined", len(EXERCISES))
    logger.info("Ollama host: %s | Judge model: %s", settings.ollama_host, settings.ollama_judge_model)

    if dry_run:
        logger.info("[DRY RUN] Would generate %d exercises:", len(EXERCISES))
        for ex in EXERCISES:
            logger.info("  %s (%s) — %s / %s", ex.name, ex.slug, ex.difficulty.value, ex.pattern.value)
        return

    inserted = 0
    skipped = 0
    failed = 0

    with tqdm(total=len(EXERCISES), desc="Generating", unit="exercise") as pbar:
        for ex in EXERCISES:
            pbar.set_description(f"{ex.name[:30]}")

            # Generate LLM descriptors
            try:
                descriptors = await generate_descriptors(ex, client)
            except Exception as e:
                logger.warning("LLM failed for %s: %s — using placeholder", ex.name, e)
                descriptors = {
                    "summary": f"{ex.name} — a {ex.pattern.value} exercise targeting {', '.join(m.value for m in ex.primary[:2])}.",
                    "beginner_descriptions": [
                        f"performing {ex.name.lower()} with {ex.equipment[0].value if ex.equipment else 'bodyweight'}",
                    ],
                    "cues": ["Maintain proper form throughout the movement."],
                    "common_mistake": "Poor form or using too much weight too soon.",
                }

            # Insert into DB
            async with async_session_factory() as session:
                result = await insert_exercise(session, ex, descriptors)

            if result is None:
                skipped += 1
                logger.debug("Skipped (already exists): %s", ex.slug)
            else:
                inserted += 1

            pbar.update(1)

    logger.info(
        "\nDone: %d inserted, %d skipped (already existed), %d failed",
        inserted, skipped, failed,
    )
    logger.info(
        "Next step: cd backend && python -m scripts.embed_database"
    )


# ─── Entry point ─────────────────────────────────────────────────────────────

def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--dry-run", action="store_true",
                        help="Print exercises without writing to DB or calling LLM")
    parser.add_argument("--skip-existing", action="store_true", default=True,
                        help="Skip exercises whose slug already exists in DB (default: True)")
    return parser.parse_args()


if __name__ == "__main__":
    args = parse_args()
    asyncio.run(run(dry_run=args.dry_run, skip_existing=args.skip_existing))
