"""
Exercise dataset generator — seeds PostgreSQL with 150 exercises.

Strategy:
  1. Taxonomy (name, muscles, equipment, difficulty) is HARDCODED — these are
     factual and must be accurate. The LLM is not trusted for structured data.
  2. Descriptor texts (summary, beginner descriptions, cues, mistakes) are
     GENERATED by the local LLM — this is where colloquial, amateur language
     is critical for the semantic search to work.

Usage:
    cd backend
    python -m scripts.generate_dataset              # full run
    python -m scripts.generate_dataset --dry-run    # print exercises, no DB writes
    python -m scripts.generate_dataset --skip-existing  # skip already-inserted slugs

Prerequisites:
    1. PostgreSQL running with schema applied (run the app once to create tables)
    2. Ollama running with the judge model pulled:
           ollama pull gemma4:e4b
    3. pip install -r requirements.txt
"""

